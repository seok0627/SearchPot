package com.jys.searchpot;

public class Store {
    public String profile;
    public String storeName;
    public String insUrl;
    public String webUrl;
    public String sellUrl;

    public Store(){}

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getStoreName() {
        return storeName;
    }

    public void setStoreName(String storeName) {
        this.storeName = storeName;
    }

    public String getInsUrl() {
        return insUrl;
    }

    public void setInsUrl(String insUrl) {
        this.insUrl = insUrl;
    }

    public String getWebUrl() {
        return webUrl;
    }

    public void setWebUrl(String webUrl) {
        this.webUrl = webUrl;
    }

    public String getSellUrl() {
        return sellUrl;
    }

    public void setSellUrl(String sellUrl) {
        this.sellUrl = sellUrl;
    }
}